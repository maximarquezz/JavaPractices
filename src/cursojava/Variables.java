package cursojava;

public class Variables {

    public static void main(String[] args) {
        
        //Tipos primitivos
        
        //ENTEROS: del más grande al más chico
        long ent1 = 100000L;
        int ent2 = 25439;
        short ent3 = 293;
        byte ent4 = 127;
        
        //DECIMALES: del más grande al más chico
        float dec1 = 12.35f;
        double dec2 = 6.2;
        
        //OTROS
        char caracter = 'a';
        boolean valor = true;
          
        //Formas de declarar una variable
        
        //Declaración e inicialización en la misma línea
        int num1 = 30;
        int num2 = 30;
        int num3 = 30;
        
        //Declaración y luego inicialización
        int num4;
        num4 = 30;
        
        //Declaración de múltiples variables de un mismo tipo de dato
        int num5, num6, num7;
        
        //Asignación de un mismo valor a múltiples variables
        num5 = num6 = num7 = 50;
    }
    
}
